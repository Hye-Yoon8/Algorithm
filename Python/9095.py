# 백준 9095번 - 1, 2, 3 더하기

'''
[1 : 1개] 1 = 1                             [2 : 2개] 2 = 1 + 1 = 2

[3 : 3개] 3 = 1 + 1 + 1 = 3
            = 1 + 2     = 2 + 1

[4 : 7개] 4 = 1 + 1 + 1 + 1
            = 2 + 1 + 1 = 1 + 2 + 1 = 1 + 1 + 2
            = 2 + 2     = 3 + 1     = 1 + 3

[5 : 13개] 5 = 1 + 1 + 1 + 1 + 1
             = 2 + 1 + 1 + 1 = 1 + 2 + 1 + 1 = 1 + 1 + 2 + 1 = 1 + 1 + 1 + 2
             = 2 + 2 + 1     = 2 + 1 + 2     = 1 + 2 + 2
             = 3 + 1 + 1     = 1 + 3 + 1     = 1 + 1 + 3
             = 3 + 2         = 2 + 3
[6 : 24개], [7 : 44개] ...

d[n] = d[n - 1] + d[n - 2] + d[n - 3]
'''

import sys

T = int(sys.stdin.readline())

for i in range(T):
    n = int(sys.stdin.readline())
    d = [0] * (n + 1)

    for j in range(1, n + 1):
        if j <= 2:
            d[j] = j
        elif j == 3:
            d[j] = 4
        else:
            d[j] = d[j-1] + d[j-2] + d[j-3]
    
    print(d[n])